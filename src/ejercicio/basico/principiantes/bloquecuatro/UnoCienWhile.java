package ejercicio.basico.principiantes.bloquecuatro;

public class UnoCienWhile {

	public static void main(String[] args) {
		// Que muestre los números del 1 al 100 utilizando la instrucción while.
		int contador=1;
		while (contador<101) {
			System.out.println(contador);
			contador ++;
		}
	}

}
