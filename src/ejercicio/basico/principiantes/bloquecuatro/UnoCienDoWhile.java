package ejercicio.basico.principiantes.bloquecuatro;

public class UnoCienDoWhile {

	public static void main(String[] args) {
		//que muestre los números del 1 al 100 utilizando la instrucción do..while
		int contador=1;
		do {
			System.out.println(contador);
			contador++;
		} while (contador<101);
		
	}

}
