package ejercicio.basico.principiantes.bloquecuatro;

public class CienUnoWhile {

	public static void main(String[] args) {
		// que muestre los números del 100 al 1 utilizando la instrucción while.
		int contador=100;
		while (contador > 0) {
			System.out.println(contador);
			contador--;
		}
	}

}
